package section5;

public class ince_decri_Operators {

	public static void main(String[] args) {
		int a=10;
		
		int psti=a++;
		System.out.println("Pst-Increment Operator "+psti);
		
		//a=11		
		int prei=++a;
		System.out.println("Pre-Increment Operator "+prei);
		
		//a=12
		int pstd=a--;
		System.out.println("Pst-Increment Operator "+pstd);
		
		
		int pred=--a;
		System.out.println("Pre-Increment Operator "+pred);
		
		
	}
}
